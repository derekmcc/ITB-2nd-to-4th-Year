//start class
public class Lab1Part2 {
	//start main method
    public static void main(String args[]) {
    	
    	//array of characters
    	String firstName[] = {"D","e","r","e","k"};
    	
    	//start for loop
    	for (int i=firstName.length-1; i>=0; i--){
    		//print the array
    		System.out.print(firstName[i]);
    	}//end for loop
    	
    	//move the cursor to the next line
    	System.out.println();
    }//end main method   
    
}//end class